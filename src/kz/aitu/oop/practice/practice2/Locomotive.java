package kz.aitu.oop.practice.practice2;

// Класс "Локомотив"
public class Locomotive {
    private int power; // Мощность локомотива

    // Конструктор для инициализации мощности локомотива
    public Locomotive(int power) {
        this.power = power;
    }

    // Геттер для получения мощности локомотива
    public int getPower() {
        return power;
    }
}


